@UIApplicationMain
class AppDelegate: UIResponder, UIApplicationDelegate {
    var window: UIWindow?

    func application(_ application: UIApplication, didFinishLaunchingWithOptions launchOptions: [UIApplicationLaunchOptionsKey: Any]?) -> Bool {
        // Override point for customization after application launch.
        FirebaseApp.configure()
        IQKeyboardManager.sharedManager().enable = true
        
        let onesignalInitSettings = [kOSSettingsKeyAutoPrompt: false]
        
        // Replace 'YOUR_APP_ID' with your OneSignal App ID.
        OneSignal.initWithLaunchOptions(launchOptions,
                                        appId: "9427e300-79df-47a4-9063-d713db0609dc",
                                        handleNotificationAction: nil,
                                        settings: onesignalInitSettings)
        
        OneSignal.inFocusDisplayType = OSNotificationDisplayType.notification;
        
        // Recommend moving the below line to prompt for push after informing the user about
        //   how your app will use them.
        OneSignal.promptForPushNotifications(userResponse: { accepted in
            print("User accepted notifications: \(accepted)")
        })
        // Sync hashed email if you have a login system or collect it.
        //   Will be used to reach the user at the most optimal time of day.
        // OneSignal.syncHashedEmail(userEmail)
        
        // Inside your application(application:didFinishLaunchingWithOptions:)
        
        // get your storyboard
        let storyboard = UIStoryboard(name: "Main", bundle: nil)
        
        // instantiate your desired ViewController
        let homeController = storyboard.instantiateViewController(withIdentifier: "homePage") as! ViewController
        let mainController = storyboard.instantiateViewController(withIdentifier: "mainTabbar")
        
        // Because self.window is an optional you should check it's value first and assign your rootViewController
        if let window = self.window {
            if (UserDefaults.standard.bool(forKey: Constants.isLogin)) {
                window.rootViewController = mainController
            } else {
                window.rootViewController = homeController
            }
        }
        return true
    }

